package vednovak_zadaca_1.leaderboard;

public class CardLeaderboard {
    public String clubName;
    public int firstYellowCards;
    public int secondYellowCards;
    public int redCards;
    public int allCards;

    public String getClubName() {
        return clubName;
    }

    public void setClubName(String clubName) {
        this.clubName = clubName;
    }

    public int getFirstYellowCards() {
        return firstYellowCards;
    }

    public void setFirstYellowCards(int firstYellowCards) {
        this.firstYellowCards = firstYellowCards;
    }

    public int getSecondYellowCards() {
        return secondYellowCards;
    }

    public void setSecondYellowCards(int secondYellowCards) {
        this.secondYellowCards = secondYellowCards;
    }

    public int getRedCards() {
        return redCards;
    }

    public void setRedCards(int redCards) {
        this.redCards = redCards;
    }

    public int getAllCards() {
        return allCards;
    }

    public void setAllCards(int allCards) {
        this.allCards = allCards;
    }
}
